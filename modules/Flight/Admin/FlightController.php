        public function flightTimingsBulkUpload(Request $request) {
            if ($request->input('submit') != null ){

                $file = $request->file('file');
                $flight_id = $request->input('flight_id');
          
                // File Details 
                $filename = $file->getClientOriginalName();
                $extension = $file->getClientOriginalExtension();
                $fileSize = $file->getSize();
          
                // Valid File Extensions
                $valid_extension = array("csv");
          
                // 2MB in Bytes
                $maxFileSize = 2097152; 
          
                // Check file extension
                if(in_array(strtolower($extension),$valid_extension)) {
          
                  // Check file size
                  if($fileSize <= $maxFileSize){
          
                    // File upload location
                    $location = 'csv-uploads';
                    
                    // Upload file
                    $file->move($location,$filename);
          
                    // Import CSV to Database
                    $filepath = public_path($location."/".$filename);

                    // Reading file
                    $file = fopen($filepath,"r");
        
                    while (($getData = fgetcsv($file, 10000, ",")) !== FALSE)
                    {
                        // Insert to MySQL database
                        $data = new FlightTimings;
                        $data->flight_id = $flight_id;
                        $data->depart_time = $getData[0]." ".$getData[1].":00";
                        $data->arrival_time = $getData[2]." ".$getData[3].":00";
                        $data->status = 0;
                        $data->save();
                    }

                    return back()->with('success', __('Flight Timings updated'));
                  } else {
                    return back()->with('error', __('File too large. File must be less than 2MB'));
                  }
        
                } else {
                   return back()->with('error', __('Invalid File Extension'));
                }
          
            }
        }
